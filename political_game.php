						echo "<hr>";
						echo "SHARED BY: $bullet->shared <br>";
					
					}
					
					echo "</div>";					
				echo "</div>";
			}
		}
	}
	else {
		echo "No bullets yet.<br>";
	}
	include("footer.php");
}

